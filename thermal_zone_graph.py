def directed_edge_index() -> list[tuple[int, int]]:
    """Return both directions for frameworks expecting directed graph edges."""
    return [(i, j) for i, j, _ in THERMAL_ZONE_EDGES] + [
        (j, i) for i, j, _ in THERMAL_ZONE_EDGES
    ]


def adjacency_list() -> dict[int, list[int]]:
    neighbors: dict[int, set[int]] = {idx: set() for idx in range(len(THERMAL_ZONES))}
    for i, j, _ in THERMAL_ZONE_EDGES:
        neighbors[i].add(j)
        neighbors[j].add(i)
    return {idx: sorted(values) for idx, values in neighbors.items()}
